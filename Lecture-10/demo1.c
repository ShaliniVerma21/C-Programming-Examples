//Ascii values

#include <stdio.h>
#include <conio.h>

int main()
{
    //create character variables
    char v1='A';
    char v2='a';
    char v3='&';
    char v4='@';
    char v5='!';

    //print numeric value
    printf("\n v1(A) : %d",v1);
    printf("\n v2(a) : %d",v2);
    printf("\n v3(&) : %d",v3);
    printf("\n v4(@) :%d",v4);
    printf("\n v5(!) : %d",v5);
}